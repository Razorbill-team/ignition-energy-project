SELECT plan_id, 
slot_ts, 
date_from, 
date_to, 
datetime, 
ch_bess_from_pv as "BESS Charge  PV" , 
ch_bess_from_wt as "BESS Charge WT", 
ch_bess_from_grid as "BESS Charge Grid", 
ch_bess_from_gas as "BESS Charge Gas", 
ch_bess_from_fcr_dn as "BESS Charge FCR", 
ch_bess_from_mfrr_dn as "BESS Charge mFRR dw", 
res_bess_fcr_up as "BESS AN FCR reservation up", 
res_bess_fcr_dn as "BESS AN FCR reservation dw", 
res_bess_mfrr_up as "BESS AN MFRR reservation up", 
res_bess_mfrr_dn as "BESS AN MFRR reservation dw", 
out_bess as "Band out BESS (discharge)", 
out_wt as "Band out WT (To grid from WT)", 
out_pv as "Band out PV (To grid from PV)", 
out_gas as "Band out GAS", 
eband_out_bess_dch as "EBand out BESS (discharge)",
eband_out_wt as "EBand out WT (To grid from WT)",
eband_out_pv as "EBand out PV (To grid from PV)",
eband_out_gas as "EBand out GAS",
